
public class Counter {
	public int count;
	
	public Counter(){
		count=0;
	}

	public void setToZero(){
		count=0;
	}
	
	public void increaseByOne(){
		count=count+1;
	}
	
	public void decreaseByOne(){
		count=count-1;
	}
	
	public void displayCount(){
		System.out.print(count);
	}
	
	public void countValue(){
		System.out.print(count);
	}
	}
